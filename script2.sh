# Mostrar información detallada del sistema operativo y del kernel
uname -a

# Mostrar información detallada del CPU (arquitectura, núcleos, hilos, velocidad, etc.)
lscpu

# Mostrar el uso de la memoria RAM en un formato legible (human-readable)
free -h

# Mostrar el uso del disco (espacio usado y disponible) en formato legible
df -h
